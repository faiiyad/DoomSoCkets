// Returns 1 if the tile at (x, y) is a wall or out of bounds, 0 if walkable
int mapIsWall(int x, int y) {
    if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT) return 1;
    return worldMap[y][x] != 0;
}